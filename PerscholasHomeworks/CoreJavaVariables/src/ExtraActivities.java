public class ExtraActivities {


    public static void main(String[] args) {

        // This examble demonstrates the use of Char Uni Code
        char euroSymbol = '\u20AC';
        byte coffeePrice = 10;
        char copyrightSymbol = '\u00A9';
        char trademarkSymbol = '\u2122';
        System.out.println("the price of coffee is: " + euroSymbol + coffeePrice );

        System.out.println("Copy right: " + copyrightSymbol);
        System.out.println("Trade Mark:Johar" + trademarkSymbol);

        int x = 10;


        System.out.println("x++ -> " + x++);
        System.out.println("++x -> " + ++x);

        ExtraActivities obj = new ExtraActivities();

        // instanceof checks whether the instance belongs to the class/sub-class/interface
        System.out.println(obj instanceof ExtraActivities);

        // Left shift assignment z<<=5 or  z= z<<5

        int z = 5;
        int h = z << 2;
        System.out.println("Left shift assignment " +h);



    }
}

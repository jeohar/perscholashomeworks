package glab303_2_1;

public class AssignmentOperatorsDemo {


    public static void main(String[] args) {

        // Assigning primitive values
        int j, k;
        j = 10;
        j = 5;
        k = j;
        System.out.println("J is : " + j);
        System.out.println("k is : " + k);
            // Multiplication Assignments

        k = j = 10; // (k = (j= 10))
        System.out.println("j is :" + j);
        System.out.println("k is :" + k);





    }
}

package ExtraActivities;

public class loop2 {
    public static void main(String[] args) {
        /*
        •	•	Iterate over an array of integers and calculate their sum using a for loop.
         */
        int count = 0;
        for(int i =1; i<=10; i++){

            count = count + i;
        }
        System.out.println(count);
    }
}
